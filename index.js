// Function expression called divide
const divide = function() {
    return 2000 / 100;
  };
  
  // Arrow function called square
  const square = (num) => {
    return num * num;
  };
  
  // Arrow function called add
  const add = (num1, num2) => {
    return num1 + num2;
  };
  
  // Example usage
  console.log(divide()); // Should output 20
  console.log(square(5)); // Should output 25
  console.log(add(10, 15)); // Should output 25
  